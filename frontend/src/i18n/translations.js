export function translate(key, lang, params) {
  const entry = translations[key];
  if (!entry) return key;
  let text = entry[lang] ?? entry[DEFAULT_LANGUAGE] ?? key;
  if (params) {
    for (const [name, value] of Object.entries(params)) {
      text = text.replaceAll(`{{${name}}}`, value);
    }
  }
  return text;
}
